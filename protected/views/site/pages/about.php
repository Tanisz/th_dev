<p class="lead text-center">
A TaleHunters fordítócsapat 2015. július 10-én formálódott a The Wolf Among Us és Game of Thrones Telltale-játékok néhány fordítójából, ezért frissen alakult csapat lévén ezeket a projekteket is már magunkénak tudhatjuk.
<br>
A csapatalapítás ötlete a fent említett projektek vezetőjétől, Farting Squirreltől ered, majd végül Forró 'Fantom' Fanni segítségével, hosszas ötleteléseken át megszületett a csapatnevünk, és így maga a csapat.
<br>
Célunk elsősorban a Telltale Games játékainak a honosítása. Nem kizárólag, de elnézve a TG jövőbeli terveit, másra nem nagyon fog időnk jutni. Ez mind akár sok is lehet egyetlen fordítócsapatnak, de külső fordítók segítségével fogjuk a projekteinket megvalósítani. Így van lehetőségünk a Telltale összes aktuális játékának a honosítására, hogy minél hamarabb élvezhessék a kiadó játékait azok is, akik csak a magyar lokalizációra várnak.
</p>

